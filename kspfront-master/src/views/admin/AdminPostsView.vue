  try {
    const response = await request.get(`/api/admin/management/post/${sectionId}/tags`)
    if (Array.isArray(response)) {
      tags.value = response.map(tag => ({
        id: tag.id,
        name: tag.tagName || `Ê†áÁ≠æ${tag.id}`
      }))
